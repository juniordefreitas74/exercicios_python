'''
A diferença de lista para tupla é que tupla é imutavel 
9não consegue alterar seus valores
'''
frutas = ('laranja', 'pera', 'banana', 'uva',
          )  # usa-s uma virgula no final indicando que é uma tupla
print(frutas)
